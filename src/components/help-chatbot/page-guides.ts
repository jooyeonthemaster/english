// Page-specific help guide data for the chatbot
// Each guide provides step-by-step instructions for every feature on that page

export interface GuideStep {
  title: string;
  steps: string[];
}

export interface PageGuide {
  path: string;
  /** Match mode: "exact" = path === current, "prefix" = current.startsWith(path) */
  match: "exact" | "prefix";
  title: string;
  icon: string;
  description: string;
  features: string[];
  guides: GuideStep[];
  tips?: string[];
}

// ─── Helper: find the best matching guide for a given pathname ───
export function findGuideForPath(pathname: string): PageGuide | null {
  // 1. Try exact match first
  const exact = PAGE_GUIDES.find((g) => g.match === "exact" && g.path === pathname);
  if (exact) return exact;

  // 2. Try prefix match, longest path first
  const prefixMatches = PAGE_GUIDES
    .filter((g) => g.match === "prefix" && pathname.startsWith(g.path))
    .sort((a, b) => b.path.length - a.path.length);

  return prefixMatches[0] || null;
}

